def write_mea_itp(filename, resname, beads, bonds, angles, b0):
    n_beads = len(beads)
    with open(filename, 'w') as f:
        f.write(f"; MARTINI v2 MEA lipid chain topology — {resname}\n")
        f.write("; Generated by gen_sys.py\n\n")
        f.write("[ moleculetype ]\n; name  nrexcl\n")
        # nrexcl=1: exclude only directly bonded (1-2) pairs within the chain.
        f.write(f"  {resname}  2\n\n")
        f.write("[ atoms ]\n")
        f.write("; nr  type  resnr  resname  atom  cgnr  charge  mass\n")
        for local_idx, (name, mtype, chg, mass) in enumerate(beads, 1):
            # mtype is now "CGH" for the ghost bead (set in ff_data.LIPID_BEADS)
            # so no override needed — mtype maps directly to forcefield.itp atomtype
            f.write(f"  {local_idx:<3d} {mtype:<5s}  1  {resname:<5s}"
                    f"  {name:<6s} {local_idx:<3d}  {chg:.1f}  {mass:.1f}\n")
        f.write("\n")
        f.write("[ bonds ]\n")
        f.write("; i  j  funct  b0(nm)  kb(kJ/mol/nm^2)\n")
        for idx, (i, j) in enumerate(bonds):
            kb = 3800.0
            f.write(f"  {i}  {j}  1  {b0:.4f}  {kb:.1f}\n")
        f.write("\n")
        f.write("[ angles ]\n")
        f.write("; i  j  k  funct  theta0(deg)  ka(kJ/mol/rad^2)\n")
        f.write("; funct=2 = G96 cosine harmonic; theta0=180 deg for straight chain\n")
        for (i, c, k) in angles:
            f.write(f"  {i}  {c}  {k}  2  180.0  25.0\n")
        f.write("\n")
        f.write("[ exclusions ]\n")
        for j in range(2, n_beads + 1):
            f.write(f"  1  {j}\n")
        f.write("\n")
        f.write("[ position_restraints ]\n")
        f.write("; atom  funct  fcx  fcy  fcz\n")
        f.write(f"  1     1      5000  5000  5000\n\n")

def load_single_odorant(resname):
    ODORANT_DIR = "./inputs/odorants/"
    gro_path = ODORANT_DIR + f"{resname}_c.gro"
    if not os.path.exists(gro_path):
        raise FileNotFoundError(
            f"Odorant .gro not found: {gro_path}\n"
            f"Expected file: inputs/odorants/{resname}.gro"
        )
    _, beads, _ = parse_gro(gro_path)
    cx = sum(b['x'] for b in beads) / len(beads)
    cy = sum(b['y'] for b in beads) / len(beads)
    cz = sum(b['z'] for b in beads) / len(beads)
    for b in beads:
        b['x'] = round(b['x'] - cx, 6)
        b['y'] = round(b['y'] - cy, 6)
        b['z'] = round(b['z'] - cz, 6)

    # Annotate with MARTINI type/charge/mass from the template
    template = ODORANT_TEMPLATES.get(resname, [])
    for b, (name, mtype, chg, mass) in zip(beads, template):
        b['_mtype']  = mtype
        b['_charge'] = chg
        b['_mass']   = mass

    r_eff = max(
        math.sqrt(b['x']**2 + b['y']**2 + b['z']**2) for b in beads
    ) + 0.235
    return beads, r_eff


def load_droplet(resname, lx, ly, z_surface):
    ODORANT_DIR = "./inputs/odorants/"
    gro_path = ODORANT_DIR + f"{resname}_droplet_c.gro"
    if not os.path.exists(gro_path):
        raise FileNotFoundError(
            f"Droplet .gro not found: {gro_path}\n"
            f"Expected: inputs/odorants/{resname}_out.gro"
        )
    _, beads, box = parse_gro(gro_path)
    r_droplet = box[0] / 2.0
    print(f"  Droplet radius (estimated from box): {r_droplet:.3f} nm")
    z_min = min(b['z'] for b in beads)
    x_cen = sum(b['x'] for b in beads) / len(beads)
    y_cen = sum(b['y'] for b in beads) / len(beads)
    dx = lx / 2.0 - x_cen
    dy = ly / 2.0 - y_cen
    dz = z_surface - z_min
    atom_list = []
    for b in beads:
        new = dict(b)
        new['atomnum'] = 0
        new['x'] = round((b['x'] + dx) % lx, 6)
        new['y'] = round((b['y'] + dy) % ly, 6)
        new['z'] = round( b['z'] + dz,       6)
        atom_list.append(new)
    return atom_list, r_droplet

def write_system_top(filename, run_dir, mol_counts, bead_types_used,
                     itp_files, box, odorant_resname=None, is_droplet=False, if_water=False, if_antifreeze=False):
    """Write the master GROMACS topology file.

    Parameters
    ----------
    filename         : output path, e.g. 'run_dir/system.top'
    run_dir          : simulation folder name (for header comment)
    mol_counts       : dict  {resname: count}, e.g. {'GRA':4, 'TMEA':1200, ...}
    bead_types_used  : list of MARTINI type strings (for forcefield.itp)
    itp_files        : list of .itp filenames to #include (molecule topologies)
    box              : (lx, ly, lz) in nm
    odorant_resname  : string or None
    is_droplet       : bool — tags the run type in the header
    """
    run_tag = "droplet" if is_droplet else "vapor"
    od_tag  = odorant_resname if odorant_resname else "none"

    with open(filename, 'w') as f:
        f.write(f"; GROMACS topology — MARTINI hair surface simulation\n")
        f.write(f"; Run folder : {run_dir}\n")
        f.write(f"; Odorant    : {od_tag}  ({run_tag} mode)\n")
        f.write(f"; Box        : {box[0]:.3f} x {box[1]:.3f} x {box[2]:.3f} nm\n")
        f.write(f"; Generated by gen_sys.py  —  do not edit manually\n\n")

        # force-field parameters
        f.write('#include "forcefield.itp"\n\n')

        # molecule topologies
        for itp in itp_files:
            f.write(f'#include "{itp}"\n')
        f.write("\n")

        if if_water:
            # water (MARTINI W — always include the definition)
            f.write('; Standard MARTINI water bead (P4)\n')
            f.write('[ moleculetype ]\n; name  nrexcl\n  W  1\n\n')
            f.write('[ atoms ]\n; nr  type  resnr  resname  atom  cgnr  charge  mass\n')
            f.write('  1   P4    1    W      W    1    0.0   72.0\n\n')
        
        if if_antifreeze:
            f.write('; MARTINI antifreeze water bead (BP4)\n')
            f.write('[ moleculetype ]\n; name  nrexcl\n  WF  1\n\n')
            f.write('[ atoms ]\n; nr  type  resnr  resname  atom  cgnr  charge  mass\n')
            f.write('  1   BP4   1    WF     WF   1    0.0   72.0\n\n')

        # system block
        f.write("[ system ]\n")
        f.write("MARTINI hair surface simulation\n\n")

        # molecules block
        f.write("[ molecules ]\n")
        f.write("; molecule_name  count\n")
        for resname, count in mol_counts.items():
            if count > 0:
                f.write(f"  {resname:<12s}  {count}\n")
        f.write("\n")