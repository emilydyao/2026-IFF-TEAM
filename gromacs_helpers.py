# GROMACS inputs/outputs
def parse_gro(filename):
    with open(filename) as f:
        lines = f.readlines()
    title  = lines[0].rstrip("\n")
    nbeads = int(lines[1].strip())
    beads  = []
    
    # Auto-detect column width from first atom line
    first = lines[2]
    # Find the two decimal points in the coordinate region (after col 20)
    coord_region = first[20:]
    dot1 = coord_region.index('.')
    dot2 = coord_region.index('.', dot1 + 1)
    col_width = dot2 - dot1  # distance between decimal points = n+5
    
    for line in lines[2:2 + nbeads]:
        c = col_width
        a = dict(
            resnum   = int(line[0:5]),
            resname  = line[5:10].strip(),
            atomname = line[10:15].strip(),
            atomnum  = int(line[15:20]),
            x = float(line[20:20+c]),
            y = float(line[20+c:20+2*c]),
            z = float(line[20+2*c:20+3*c]),
        )
        if len(line.rstrip()) > 20 + 3*c:
            try:
                vc = c - 1  # velocity width is n+4 (one fewer than position)
                a['vx'] = float(line[20+3*c:20+3*c+vc])
                a['vy'] = float(line[20+3*c+vc:20+3*c+2*vc])
                a['vz'] = float(line[20+3*c+2*vc:20+3*c+3*vc])
            except (ValueError, IndexError):
                pass
        beads.append(a)
    box = tuple(float(v) for v in lines[2 + nbeads].split()[:3])
    return title, beads, box


def write_gro(filename, title, beads, box):
    """Write a GROMACS .gro file. Atom/residue numbers wrap at 99999."""
    with open(filename, "w") as f:
        f.write(title + "\n")
        f.write(f"{len(beads):5d}\n")
        for i, a in enumerate(beads, 1):
            rn = a['resnum'] % 100000
            an = i % 100000
            if 'vx' in a:
                f.write(
                    f"{rn:5d}{a['resname']:<5s}{a['atomname']:>5s}{an:5d}"
                    f"{a['x']:8.3f}{a['y']:8.3f}{a['z']:8.3f}"
                    f"{a['vx']:8.4f}{a['vy']:8.4f}{a['vz']:8.4f}\n"
                )
            else:
                f.write(
                    f"{rn:5d}{a['resname']:<5s}{a['atomname']:>5s}{an:5d}"
                    f"{a['x']:8.3f}{a['y']:8.3f}{a['z']:8.3f}\n"
                )
        f.write(f"  {box[0]:.5f}  {box[1]:.5f}  {box[2]:.5f}\n")

# Molecule topology writers


def write_gra_itp(filename, resname, beads, bonds, angles, b,
                  kb=3800.0, ka=200.0, planar_kd=50.0, use_planar_dihedral=True):
 
    # Build adjacency for planar dihedral generation
    # For each bead i with neighbours [j, k, l], write improper i-j-k-l
    adj = {}
    if use_planar_dihedral:
        from collections import defaultdict
        _adj = defaultdict(list)
        for (a_, b_) in bonds:
            _adj[a_].append(b_)
            _adj[b_].append(a_)
        adj = dict(_adj)
 
    with open(filename, 'w') as f:
        f.write("; MARTINI v2 graphene sheet topology\n")
        f.write("; Generated by gen_sys.py\n")
        f.write(f"; {len(beads)} beads/layer | b0={b:.4f} nm | kb={kb:.1f} | ka={ka:.1f}\n")
        f.write(f"; planar_dihedral={'ON (equilibration only)' if use_planar_dihedral else 'OFF (production)'}\n\n")
 
        # --- moleculetype ---
        f.write("[ moleculetype ]\n")
        f.write("; name  nrexcl\n")
        f.write(f"  {resname}  2\n\n")
        # nrexcl=2: exclude 1-2, 1-3, 1-4 within the aromatic sheet
 
        # --- atoms ---
        f.write("[ atoms ]\n")
        f.write("; nr   type  resnr  resname  atom     cgnr  charge  mass\n")
        for a in beads:
            f.write(f"  {a['atomnum']:<5d} C1    1     {resname:<5s}"
                    f"  {a['atomname']:<7s}  {a['atomnum']:<5d}  0.0   36.0\n")
        # mass 36 g/mol: each CG bead represents ~3 carbons (3 × 12 = 36)
 
        # --- bonds ---
        f.write("\n[ bonds ]\n")
        f.write("; i      j      funct  b0(nm)   kb(kJ/mol/nm²)\n")
        for (i, j) in bonds:
            f.write(f"  {i:<6d} {j:<6d} 1      {b:.4f}   {kb:.1f}\n")
 
        # --- angles ---
        f.write("\n[ angles ]\n")
        f.write("; i      center  k      funct  theta0(deg)  ka(kJ/mol/rad²)\n")
        f.write("; funct=2 = G96/cosine harmonic, standard for MARTINI\n")
        for (i, c, k) in angles:
            f.write(f"  {i:<6d} {c:<6d} {k:<6d} 2      120.0        {ka:.1f}\n")
 
        # --- dihedrals (planar restraint, equilibration only) ---
        if use_planar_dihedral:
            f.write("\n[ dihedrals ]\n")
            f.write("; Improper planar dihedrals — keep sheet flat during equilibration\n")
            f.write("; funct=2: harmonic improper  phi0=0 deg  kd=planar_kd kJ/mol\n")
            f.write("; REMOVE for production runs and any gmx rdf / PBC-sensitive analysis\n")
            f.write("; i      j      k      l      funct  phi0   kd\n")
            written = set()
            for i, nbrs in adj.items():
                if len(nbrs) >= 3:
                    j, k, l = sorted(nbrs)[:3]
                    key = (i, j, k, l)
                    if key not in written:
                        written.add(key)
                        f.write(f"  {i:<6d} {j:<6d} {k:<6d} {l:<6d} 2      0.0    {planar_kd:.1f}\n")
 
        f.write("\n")

